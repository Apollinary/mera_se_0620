public class NumberComparision {


    public static void main(String[] args) {
        Integer i1 = 1;
        Integer i2 = 1;
        Integer i3 = 2;


        boolean lesser = i1 < i2;
        System.out.println(lesser);

        boolean lesserOrEquals = i1 <= i2;
        System.out.println(lesserOrEquals);


         boolean greater = i3 > i1;
        System.out.println(greater);

         boolean greaterOrEquals = i3 >= i1;
        System.out.println(greaterOrEquals);


    }
}
